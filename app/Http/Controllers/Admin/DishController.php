     */
    public function store(Request $request)
    {
        $data = $request->all();
        $data['restaurant_id'] = Auth::id();
        $newDish = new Dish();

        $newDish->fill($data);
        $newDish->save();

        return redirect()->route('admin.menu.index');
    }

